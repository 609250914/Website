public class Admin {
    String controller_id;
    String controller_name;
    String controller_pwd;
    String controller_tel;
    String controller_address;

    public String getController_id() {
        return controller_id;
    }

    public void setController_id(String controller_id) {
        this.controller_id = controller_id;
    }

    public String getController_name() {
        return controller_name;
    }

    public void setController_name(String controller_name) {
        this.controller_name = controller_name;
    }

    public String getController_pwd() {
        return controller_pwd;
    }

    public void setController_pwd(String controller_pwd) {
        this.controller_pwd = controller_pwd;
    }

    public String getController_tel() {
        return controller_tel;
    }

    public void setController_tel(String controller_tel) {
        this.controller_tel = controller_tel;
    }

    public String getController_address() {
        return controller_address;
    }

    public void setController_address(String controller_address) {
        this.controller_address = controller_address;
    }
}
